#include "../libft.h"

int	ft_intlen(int i)
{
	int	len;

	len = 0;
	if (i == 0)
		return (1);
	if (i == -2147483648)
		return (11);
	if (i < 0)
	{
		len++;
		i *= -1;
	}
	while (i > 0)
	{
		i = i / 10;
		len++;
	}
	return (len);
}
